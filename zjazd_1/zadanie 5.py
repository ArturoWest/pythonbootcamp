Podaj_liczbe = float(input("Podaj liczbę: "))

print ("czy liczba jest większa niż 10", Podaj_liczbe > 10)
print ("czy liczba jest równa 15", Podaj_liczbe <= 15)
print ("czy liczba jest podzielna przez 2", Podaj_liczbe % 2 == 0)

print (f"""wieksza od 10: {Podaj_liczbe > 10}
Mniejsza rowna 15: {Podaj_liczbe <= 15}
Podzielna przez 2: {Podaj_liczbe % 2 == 0}
""")
